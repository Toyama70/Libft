#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	while (n--)
	{
		if ((unsigned char)c == *ptr)
			return (ptr);
		ptr++;
	}
	return (NULL);
}
